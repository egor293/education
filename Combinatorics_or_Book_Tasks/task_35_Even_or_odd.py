def even_or_odd(num):
    if num%2==0:return True
    else:return False

assert even_or_odd(1)==False, even_or_odd(1)
assert even_or_odd(0)==True, even_or_odd(0)
assert even_or_odd(1312)==True, even_or_odd(1312)
assert even_or_odd(-1000)==True, even_or_odd(-1000)
assert even_or_odd(13)==False, even_or_odd(13)
